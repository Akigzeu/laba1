def area(a, h):
    '''Принимаем числа (a - сторона треугольника) и (h - высота проведенная к стороне a) '''
    return a * h / 2
    '''Возвращаем площадь треугольника'''
def perimeter(a, b, c):
    '''Принимаем числа a,b,c - стороны треугольника'''
    return a + b + c
    '''Возвращаем периметр треугольника'''